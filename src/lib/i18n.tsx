/** Intl locale for date formatting (Thai forced to Gregorian calendar). */
export function localeOf(lang: Lang): string {
  switch (lang) {
    case "th":
      return "th-TH-u-ca-gregory";
    case "zh-CN":
      return "zh-CN";
    case "zh-TW":
      return "zh-TW";
    default:
      return "en-US";
  }
}

