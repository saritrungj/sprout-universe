export type TParams = Record<string, string | number>;
export type TFn = (key: string, params?: TParams) => string;

function interpolate(template: string, params?: TParams): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) =>
    k in params ? String(params[k]) : `{${k}}`,
  );
}

function makeT(lang: Lang): TFn {
  const dict = DICTS[lang];
  return (key, params) => {
    const template = EXTRA[lang]?.[key] ?? dict[key] ?? EXTRA.en[key] ?? en[key] ?? key;
    return interpolate(template, params);
  };
}

type I18nValue = { lang: Lang; locale: string; t: TFn };

const I18nContext = createContext<I18nValue>({
  lang: "en",
  locale: "en-US",
  t: makeT("en"),
});

export function I18nProvider({
  lang,
  children,
  titleKey = "app.title",
}: {
  lang: Lang;
  children: ReactNode;
  /** Translation key used for document.title (defaults to the planner title). */
  titleKey?: string;
}) {
  useEffect(() => {
    document.documentElement.lang = lang;
    document.title = makeT(lang)(titleKey);
  }, [lang, titleKey]);

  const value = useMemo<I18nValue>(
    () => ({ lang, locale: localeOf(lang), t: makeT(lang) }),
    [lang],
  );
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useT(): I18nValue {
  return useContext(I18nContext);
}
